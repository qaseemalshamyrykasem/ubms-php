i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: 'ar',
    lng: localStorage.getItem('ubms_locale') || 'ar',
    detection: {
      order: ['localStorage', 'navigator'],
      lookupLocalStorage: 'ubms_locale',
      caches: ['localStorage'],
    },
    interpolation: { escapeValue: false },
  })

// Sync direction
const applyDirection = (lang: string) => {
  document.documentElement.lang = lang
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr'
}
applyDirection(i18n.language?.startsWith('ar') ? 'ar' : 'en')
i18n.on('languageChanged', (lng) => {
  applyDirection(lng.startsWith('ar') ? 'ar' : 'en')
  localStorage.setItem('ubms_locale', lng)
})

export default i18n
